// Turn posts data geoson
export const toGeoson = (posts) => {
  const features = posts.map((post) => {
    const lat = +post.lat;
    const lng = +post.lng;

    const result = {
      type: "Feature",
      properties: {
        kind: post.kind,
        id: post.id,
        class: post.kind.split(" ").join(""),
      },
      geometry: {
        type: "Point",
        coordinates: [lng, lat],
      },
    };

    return result;
  });

  return features;
};

